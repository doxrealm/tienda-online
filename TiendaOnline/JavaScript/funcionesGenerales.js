//*************************************************************************************************************************************************************
function DetenerSecuencia ()
{  
    if(SecuenciaEjecutandose)
	{  
        clearTimeout(SecuenciaID)  
	}
    
	SecuenciaEjecutandose = false;  
    
	imagen = 0;  
}  
//*************************************************************************************************************************************************************


 
//*************************************************************************************************************************************************************
function MostrarSecuencia () 
{  
    if (CompruebaVersion()) 
	{  
    	document.images["imgPortada"].src = imagenes[imagen].src;
        
		imagen++;  
		
		if (imagen == 5)
		{  
			imagen = 1;
		}
    } 
	
	SecuenciaID = setTimeout("MostrarSecuencia()", 10000);
	
    SecuenciaEjecutandose = true;
}  
//*************************************************************************************************************************************************************  


//*************************************************************************************************************************************************************
function IniciarSecuencia () 
{  
    DetenerSecuencia();
	  
    imagen = 1;
	
    MostrarSecuencia();  
}  
