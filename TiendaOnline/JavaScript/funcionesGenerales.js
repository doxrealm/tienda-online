// VALIDAR NICK - DEBE CONTENER MINIMO 6 CARACTERES (NO ESPECIALES)
//*************************************************************************************************************************************************************
function nickname(nick, labelNick)
{
	var regexp = /^([a-zA-Z0-9_-]{3,15})$/;
	var correcto = false;
	
	
	if((nick.match(regexp)) && (nick != ""))
	{
		labelNick.innerHTML = "";
		
		correcto = true;
	}
	else
	{
		labelNick.innerHTML = "El nickname debe contener entre 3 y 15 caracteres (no especiales)";
	}
	
	return correcto;
}
//*************************************************************************************************************************************************************


// VALIDAR CONTRASEÑA - DEBE CONTENER UNA LETRA Y UNA MAYÚSCULA COMO MINIMO, Y ENTRE 6 Y 10 CARACTERES (NO ESPCIALES)
//*************************************************************************************************************************************************************
function passw0rd(pas, labelPas)
{
	var regexp = /(?!^[0-9]*$)(?!^[a-zA-Z]*$)^([a-zA-Z0-9]{6,15})$/;
	var correcto = false;

	
	if((pas.match(regexp)) && (pas != ""))
	{
		labelPas.innerHTML = "";
		
		correcto = true;
	}
	else
	{
		labelPas.innerHTML = "El password debe tener entre 6 y 15 caracteres (no especiales), por lo menos un d&iacute;gito y una may&uacute;scula";
	}
	
	return correcto;
}
